/** Minimum horizontal movement (density-independent) before swipe-to-page activates. */
export const READER_SWIPE_ACTIVE_OFFSET_X = 36;

/** If vertical movement exceeds this before activation, horizontal swipe fails (lets body scroll). */
export const READER_SWIPE_FAIL_OFFSET_Y = 14;

/** Minimum horizontal distance (pt) to count as a completed page swipe. */
export const READER_SWIPE_DISTANCE_PT = 56;

/** Minimum horizontal velocity (pt/s) to accept a quick flick as a page change. */
export const READER_SWIPE_VELOCITY_PT_PER_S = 520;
